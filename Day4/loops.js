/* loops */

/* Loops are used in JavaScript to perform repeated tasks based on a condition.
 Conditions typically return true or false when analysed.
  A loop will continue running until the defined condition returns false  */

/* There are basically two types of loops in most computer programming languages,
   namely, entry controlled loops and exit controlled loops. */

for (let i = 0; i <= 20; i += 2) {
  console.log(i);
}
for (let i = 1; i <= 20; i += 2) {
  console.log(i);
}
for (let i = 100; i >= 0; i++) {
  console.log(i);
}
for (let i = 50; i >= 0; i += 10) {
  console.log(i);
}
